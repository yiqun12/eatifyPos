/**
 * Translation function, reference Terminal.js fanyi function
 */
export const t = (input) => {
    const lang = localStorage.getItem("Google-language");
    if (lang?.includes("Chinese") || lang?.includes("中")) {
        const translation = memberTranslations.find(t => t.input.toLowerCase() === input.toLowerCase());
        return translation ? translation.output : input;
    }
    return input;
};

/**
 * Translate object keys in batch
 */
export const translateObject = (obj) => {
    const translated = {};
    Object.keys(obj).forEach(key => {
        translated[t(key)] = obj[key];
    });
    return translated;
};

/**
 * Return different currency format based on language
 */
export const formatCurrency = (amount) => {
    // Handle null, undefined, or non-numeric values
    const numAmount = parseFloat(amount);
    if (isNaN(numAmount)) {
        return '$0.00';
    }
    
    const lang = localStorage.getItem("Google-language");
    if (lang?.includes("Chinese") || lang?.includes("中")) {
        return `$${numAmount.toFixed(2)}`;
    }
    return `$${numAmount.toFixed(2)}`;
};

/**
 * Format date time
 */
export const formatDateTime = (date) => {
    if (!date) return '';
    
    const lang = localStorage.getItem("Google-language");
    const d = new Date(date.seconds ? date.seconds * 1000 : date);
    
    if (lang?.includes("Chinese") || lang?.includes("中")) {
        return d.toLocaleString('zh-CN');
    }
    return d.toLocaleString('en-US');
};

export default { t, translateObject, formatCurrency, formatDateTime, memberTranslations };